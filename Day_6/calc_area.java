// In this real-life example, we create a program to calculate the area of a rectangle (by multiplying the length and width):

public class calc_area {
    public static void main(String[] args) {
        int length = 10;
        int width = 5;
        int area;

        area = length * width;
        System.out.println("If the length is " + length);
        System.out.println("If the width is " + width);
        System.out.println("The area then is " + area);
    }
    
}

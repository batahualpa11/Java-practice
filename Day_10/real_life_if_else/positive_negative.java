// This example shows how you can use if..else to find out if a number is positive or negative:

public class positive_negative {
    public static void main(String[] args) {
        int myNum = -8;

        if (myNum < 0) {
            System.out.println("Your number is a negative!");
        } else if (myNum > 0) {
            System.out.println("your number is a positive!!");
        } else {
            System.out.println("your number is the same as 0");
        }
    }
    
}
